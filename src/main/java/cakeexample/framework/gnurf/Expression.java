package cakeexample.framework.gnurf;

import cakeexample.framework.domain.Field;
import cakeexample.framework.util.DbUtil;
import fj.F;
import fj.data.List;

import java.sql.ResultSet;
import java.util.Optional;

import static cakeexample.framework.util.Throwables.propagate;

public class Expression<C> {
    private final DbUtil dbUtil;
    private final Table<C> table;

    public Expression(DbUtil dbUtil, Table<C> table) {
        this.dbUtil = dbUtil;
        this.table = table;
    }

    public List<C> selectAll() {
        return dbUtil.select(table.name, (ResultSet r) -> {
            F f = new FColumnToField<>(r);
            //noinspection unchecked
            return table.entityConstructor.apply(table.columns.map(f));
        });
    }

    private <T> T getValue(ResultSet r, Column<?, T> c) {
        //noinspection unchecked
        return (T) propagate(() -> r.getString(c.name));
    }

    public C update(C c) {
        throw new RuntimeException("Not implemented");
    }

    public InsertContinuation insert(C t) {
        return new InsertContinuation(dbUtil.insert(table.name, table.columns.map(c -> harmoniseTypes(c, t))));
    }

    private static <C, V> Column<C, V> harmoniseTypes(Column<C, V> c, C entity) {
        return c.withField(c.field.withValue(c.field.getter.get().f(entity)));
    }

    class FColumnToField<T> implements F<Column<?, T>, Field<?, T>> {
        private final ResultSet r;

        public FColumnToField(ResultSet r) {
            this.r = r;
        }

        @Override
        public Field<?, T> f(Column<?, T> c) {
            return c.as(getValue(r, c));
        }
    }

    public class InsertContinuation {
        public final Optional<Long> autogeneratedKey;

        public InsertContinuation(Optional<Long> autogeneratedKey) {
            this.autogeneratedKey = autogeneratedKey;
        }

        public C retrieve() {
            throw new RuntimeException("Not implemeted");
        }
    }

}
